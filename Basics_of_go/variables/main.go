package main

import "fmt"

func main(){
	// syntax: var <variable_name> <data_type> = <value>
	var name string = "a string"
	// short hand declaration
	var name2 = "another string"

	// another short hand declaration
	age := 25

	// printing the variables
	fmt.Println(age)
	fmt.Println(name2)
	fmt.Println(name)
}
